def get_dataset() -> Tuple[List[str], List[str]]:
    """
    Повертає кортеж (тексти, мітки) для подальшого тренування sklearn-моделі.

    Використання:
        texts, labels = get_dataset()
        X_train, X_test, y_train, y_test = train_test_split(texts, labels, ...)
    """
    texts = [t for t, _ in DATASET]
    labels = [l for _, l in DATASET]
    return texts, labels


def get_class_distribution() -> dict:
    """
    Повертає розподіл прикладів по класах. Використовується для діагностики
    балансу датасету під час тренування.
    """
    dist = {cls: 0 for cls in EMOTION_CLASSES}
    for _, label in DATASET:
        if label in dist:
            dist[label] += 1
    return dist
